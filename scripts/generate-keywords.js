const fs = require('fs');
const hubs = JSON.parse(fs.readFileSync('./data/hubs.json', 'utf8'));
const legacySlugs = require('./data/legacySlugs.ts').legacySlugs;

// 키워드에서 regionSlug/topicSlug 찾기
function findHubSlug(keyword) {
  for (const hub of hubs) {
    if (hub.keywords.includes(keyword)) {
      return hub.hubSlug.startsWith('region-') ? { regionSlug: hub.hubSlug.replace('region-', '') } : { topicSlug: hub.hubSlug.replace('topic-', '') };
    }
  }
  return {};
}

// slug 생성 (간단 버전)
function generateSlug(keyword) {
  return keyword
    .toLowerCase()
    .replace(/&/g, 'and')
    .replace(/\//g, '-')
    .replace(/\s+/g, '-')
    .replace(/[^\w\-가-힣]/g, '')
    .replace(/[가-힣]/g, '')
    .replace(/-+/g, '-')
    .replace(/^-|-$/g, '') || 'wedding-fair';
}

const result = keywords.map((keyword, index) => {
  const pageTitle = pageTitles[index];
  const hubInfo = findHubSlug(keyword);
  const slug = legacySlugs.includes(keyword) ? keyword : generateSlug(keyword);
  
  return {
    keyword,
    pageTitle,
    slug,
    ...hubInfo,
    type: 'detail',
    priority: 1,
    isBrand: false,
  };
});

fs.writeFileSync('./data/keywords.json', JSON.stringify(result, null, 2), 'utf8');
